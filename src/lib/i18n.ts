export function useTranslation(profile: Profile | null) {
  const lang = (profile?.language as Language) || 'en';
  const currency = (profile?.currency as Currency) || 'USD';
  const isRTL = lang === 'he';

  const t = useCallback(
    (key: keyof typeof translations['en'] | keyof typeof translations['he']) => {
      return translations[lang]?.[key] || translations['en'][key] || key;
    },
    [lang]
  );

  const formatCurrency = useCallback(
    (amount: number, showSign = true, compact = false) => {
      const formattedNumber = new Intl.NumberFormat(lang === 'he' ? 'he-IL' : 'en-US', {
        minimumFractionDigits: compact ? 0 : 2,
        maximumFractionDigits: compact ? 0 : 2,
        signDisplay: 'never',
        notation: compact ? 'compact' : 'standard',
        compactDisplay: 'short'
      }).format(Math.abs(amount));

      const symbol = currencySymbols[currency];
      if (amount < 0 && showSign) {
        return isRTL ? `${symbol}${formattedNumber}-` : `-${symbol}${formattedNumber}`;
      }
      return `${symbol}${formattedNumber}`;
    },
    [lang, currency, isRTL]
  );

  return useMemo(() => ({ t, formatCurrency, lang, isRTL }), [t, formatCurrency, lang, isRTL]);
}